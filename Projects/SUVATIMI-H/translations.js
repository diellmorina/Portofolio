// Set current language (default: English)
let currentLanguage = localStorage.getItem('language') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    setLanguage(currentLanguage);
});

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    setLanguage(lang);
    
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
    });
    event.target.classList.add('active');
}

// Set all text to selected language
function setLanguage(lang) {
    // Update all elements with data attributes
    document.querySelectorAll('[data-en][data-sq]').forEach(element => {
        if (lang === 'en') {
            element.textContent = element.getAttribute('data-en');
        } else {
            element.textContent = element.getAttribute('data-sq');
        }
    });
    
    // Update Font Awesome icons by their data-icon attribute
    document.querySelectorAll('[data-icon]').forEach(element => {
        const iconKey = element.getAttribute('data-icon');
        if (translations[lang] && translations[lang][iconKey]) {
            const iconClass = translations[lang][iconKey];
            element.className = iconClass;
        }
    });
    
    // Update page title
    if (lang === 'sq') {
        document.title = "SUVATIMI-H | Shërbime Profesionale të Fasadës";
    } else {
        document.title = "SUVATIMI-H | Professional Facade Services";
    }
}
